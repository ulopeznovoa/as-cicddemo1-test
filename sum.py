# Definimos una función que suma dos números
def sumar(a, b):
    return a + b

# Declaramos dos variables con números a nuestra elección
numero1 = 5
numero2 = 7

# Usamos la función sumar y mostramos el resultado con print
resultado = sumar(numero1, numero2)
print(f"La suma de {numero1} y {numero2} es: {resultado}")
